# 피보나찌 수 구하기 (반복적 방법)
import time


def fib2(n):
    a = [0]
    if n > 0:
        a.append(1)
        for j in range(2, n+1):
            a.append(a[j-1] + a[j-2])
    return a[n]


for i in range(40):
    print('%2d %10d %10.5f' % (i, fib2(i), time.time()))

