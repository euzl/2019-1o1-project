# 피보나찌 수 구하기 (반복적 방법)
import time


def fib1(n):
    if n <= 1:
        return n
    else:
        return fib1(n-1) + fib1(n-2)


for i in range(40):
    print('%2d %10d %10.5f' % (i, fib1(i), time.time()))

